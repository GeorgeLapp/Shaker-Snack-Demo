/**
 * Статусы workflow покупки в вендинговом автомате.
 * Используется для управления отображением модального окна и логикой транзакции.
 * Строковые значения удобны для логов и отладки.
 */
export enum SaleWorkflowStatus {
  /**
   * Ожидание поднесения карты для оплаты.
   * UI: показать сообщение "Приложите карту".
   * Начальное состояние перед попыткой оплаты.
   */
  AwaitingCard = 'AwaitingCard',

  /**
   * Оплата успешно прошла, начинается механическая выдача товара.
   * UI: показать сообщение "Идёт выдача" и индикатор прогресса.
   * В этом состоянии можно отслеживать статус механизма выдачи.
   */
  Dispensing = 'Dispensing',

  /**
   * Товар успешно выдан, транзакция завершена успешно.
   * UI: показать подтверждение успешной выдачи и/или благодарность.
   */
  Dispensed = 'Dispensed',

  /**
   * Ошибка при проведении оплаты (например, отказ банка, чтение карты провалилось).
   * UI: показать сообщение об ошибке оплаты и варианты повторить/отмена.
   * Рекомендуется хранить причину ошибки (код/текст) для логирования.
   */
  PaymentFailed = 'PaymentFailed',

  /**
   * Ошибка при выдаче товара (например, застряла ячейка, механика не сработала).
   * UI: показать сообщение об ошибке выдачи и инструкцию для пользователя/оператора.
   * Требует логирования и, возможно, запуска компенсации/ручного вмешательства.
   */
  DispenseFailed = 'DispenseFailed',
}
